DOUBLE rsautil_normalizedEntropy(LPCBYTE data, DWORD len)
{
	DOUBLE ret = 0.0, p;
	DWORD i, hist[256] = {0};
	for (i = 0; i < len; i++)
		hist[data[i]]++;

	for(i = 0; i < ARRAYSIZE(hist); i++)
	{
		if(hist[i])
		{
			p = (DOUBLE) hist[i] / (DOUBLE) len;
			ret += p * log(p);
		}
	}
	return (ret == 0.0) ? 0.0 : (-ret / log(256.));
}

